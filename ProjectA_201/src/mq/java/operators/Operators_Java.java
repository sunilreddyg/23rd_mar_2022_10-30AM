package mq.java.operators;

public class Operators_Java {

	public static void main(String[] args) {
		
		/*
		 * 		+	Addition		Adds together two values	x + y	
		 * 		-	Subtraction		Subtracts one value from another	x - y	
		 *		*	Multiplication	Multiplies two values	x * y	
		 *		/	Division		Divides one value by another	x / y	
		 *		%	Modulus			Returns the division remainder	x % y	
		 *		++	Increment		Increases the value of a variable by 1	++x	
		 *		--	Decrement		Decreases the value of a variable by 1	--x
		 */
		
		
		int a=100;
		int b=200;
		
		System.out.println("addition is "+(a+b));
		System.out.println("subtraction is =>"+(b-a));
		System.out.println("multiplication is =>"+(a*b));
		System.out.println("Division is =>"+(b/a));
		System.out.println("Modulus is =>"+(b%a));
		System.out.println("increment value is =>"+(++a));
		System.out.println("Decrement value is =>"+(--b));
		
		
		

	}

}
